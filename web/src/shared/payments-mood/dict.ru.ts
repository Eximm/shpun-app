/* ─── Types ─────────────────────────────────────────────────────────────── */

type MoodKey = keyof typeof paymentsMoodDict;
type MoodBucket = "tiny" | "small" | "mid" | "big" | "huge" | "any";

/* ─── Helper ─────────────────────────────────────────────────────────────── */

function pickRandom<T>(arr: readonly T[], seed?: string): T {
  if (!arr.length) return arr[0];
  if (seed) {
    // Детерминированный выбор по seed — одна сессия = один и тот же текст
    let h = 0;
    for (let i = 0; i < seed.length; i++) h = Math.imul(31, h) + seed.charCodeAt(i) | 0;
    return arr[Math.abs(h) % arr.length];
  }
  return arr[Math.floor(Math.random() * arr.length)];
}

function amountToBucket(amount?: number): MoodBucket {
  if (!amount || amount <= 0) return "any";
  if (amount < 100)  return "tiny";
  if (amount < 500)  return "small";
  if (amount < 2000) return "mid";
  if (amount < 5000) return "big";
  return "huge";
}

/* ─── getMood ────────────────────────────────────────────────────────────── */

export function getMood(
  key: MoodKey,
  opts?: { amount?: number; seed?: string }
): string | null {
  const dict = paymentsMoodDict[key] as Record<string, readonly string[]>;
  if (!dict) return null;

  const bucket = amountToBucket(opts?.amount);

  // Ищем точный bucket → fallback на "any" → fallback на первый доступный
  const phrases =
    dict[bucket] ??
    dict["any"] ??
    Object.values(dict)[0];

  if (!phrases?.length) return null;
  return pickRandom(phrases, opts?.seed) ?? null;
}