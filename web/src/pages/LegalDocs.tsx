function LegalDocument({ doc }: { doc: LegalDoc }) {
  return (
    <div className="section legal-page">
      <LegalHero />
      <LegalTabs active={doc.key} />

      <div className="card legal-document-hero">
        <div className="card__body">
          <Link className="btn legal-back" to="/legal">← Все документы</Link>
          <div className="legal-document-hero__head">
            <div className="legal-document-hero__icon">{doc.icon}</div>
            <div>
              <h2 className="legal-title">{doc.title}</h2>
              <p className="p">{doc.description}</p>
              <div className="legal-meta">Редакция от {EDITION_DATE}</div>
            </div>
          </div>
        </div>
      </div>

      <div className="legal-doc-sections">
        {doc.sections.map((section) => (
          <section className="card legal-section-card" key={section.title}>
            <div className="card__body">
              <h3 className="legal-section-title">{section.title}</h3>
              {section.body?.map((paragraph) => {
                if (paragraph.startsWith("Telegram:")) {
                  return (
                    <p className="legal-p" key={paragraph}>
                      Telegram: <a href={SUPPORT_TELEGRAM} target="_blank" rel="noopener noreferrer">@shpun_staff</a>
                    </p>
                  );
                }
                if (paragraph.startsWith("Email:")) {
                  return (
                    <p className="legal-p" key={paragraph}>
                      Email: <a href={`mailto:${SUPPORT_EMAIL}`}>{SUPPORT_EMAIL}</a>
                    </p>
                  );
                }
                return <p className="legal-p" key={paragraph}>{paragraph}</p>;
              })}
              {section.list ? (
                <ul className="legal-list">
                  {section.list.map((item) => <li key={item}>{item}</li>)}
                </ul>
              ) : null}
            </div>
          </section>
        ))}
      </div>
    </div>
  );
}

export function LegalDocs() {
  const { docKey } = useParams();

  if (!docKey) return <LegalOverview />;

  const doc = getDoc(docKey);
  if (!doc) return <Navigate to="/legal" replace />;

  return <LegalDocument doc={doc} />;
}

export default LegalDocs;
